package com.ucatolica.ecommerce.enums;

/**
 * Enumeración que define los estados de respuesta posibles para las respuestas de la aplicación.
 * Los estados posibles son: success (éxito) y error (error).
 */
public enum ResponseStatus {
    /**
     * Representa un estado de respuesta exitoso.
     */
    success,

    /**
     * Representa un estado de respuesta que indica un error.
     */
    error
}
