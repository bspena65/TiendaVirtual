package com.ucatolica.ecommerce.utils;

/**
 * Clase utilitaria con métodos auxiliares.
 * Proporciona métodos de utilidad para verificar si un objeto no es nulo.
 */
public class Helper {

    /**
     * Verifica si un objeto no es nulo.
     *
     * @param obj El objeto que se desea verificar.
     * @return true si el objeto no es nulo, false en caso contrario.
     */
    public static boolean notNull(Object obj){
        // Implementación para verificar si un objeto no es nulo
        return obj != null;
    }
}
